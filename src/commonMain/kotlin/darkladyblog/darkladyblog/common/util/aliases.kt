fun String.toAlias(delimiter: Char = '-'): String =
    lowercase().map { c ->
        map[c].let { r ->
            r ?: if (c in '0'..'9' || c in 'A'..'Z' || c in 'a'..'z') c else delimiter
        }
    }.joinToString("")
